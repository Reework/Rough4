package com.shamrock.reework.activity.BloodTestModule.pojo;

public class OtherReportData {
    private String ReportFilePath;

    private String ReportName;

    private String FileType;

    private String Id;

    private String CreatedOn;

    private String ReeworkerId;

    public String getReportFilePath() {
        return ReportFilePath;
    }

    public void setReportFilePath(String reportFilePath) {
        ReportFilePath = reportFilePath;
    }

    public String getReportName() {
        return ReportName;
    }

    public void setReportName(String reportName) {
        ReportName = reportName;
    }

    public String getFileType() {
        return FileType;
    }

    public void setFileType(String fileType) {
        FileType = fileType;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getCreatedOn() {
        return CreatedOn;
    }

    public void setCreatedOn(String createdOn) {
        CreatedOn = createdOn;
    }

    public String getReeworkerId() {
        return ReeworkerId;
    }

    public void setReeworkerId(String reeworkerId) {
        ReeworkerId = reeworkerId;
    }
}
