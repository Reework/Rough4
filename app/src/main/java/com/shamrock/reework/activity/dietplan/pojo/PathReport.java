package com.shamrock.reework.activity.dietplan.pojo;

public class PathReport {
    private String BioRef;

    private String Score;

    private String SpecificDietAdivsory;

    private String ParameterName;

    private String Id;

    private String Unit;

    private String ReportType;

    private String Result;

    private String Remark;

    public String getBioRef() {
        return BioRef;
    }

    public void setBioRef(String bioRef) {
        BioRef = bioRef;
    }

    public String getScore() {
        return Score;
    }

    public void setScore(String score) {
        Score = score;
    }

    public String getSpecificDietAdivsory() {
        return SpecificDietAdivsory;
    }

    public void setSpecificDietAdivsory(String specificDietAdivsory) {
        SpecificDietAdivsory = specificDietAdivsory;
    }

    public String getParameterName() {
        return ParameterName;
    }

    public void setParameterName(String parameterName) {
        ParameterName = parameterName;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getUnit() {
        return Unit;
    }

    public void setUnit(String unit) {
        Unit = unit;
    }

    public String getReportType() {
        return ReportType;
    }

    public void setReportType(String reportType) {
        ReportType = reportType;
    }

    public String getResult() {
        return Result;
    }

    public void setResult(String result) {
        Result = result;
    }

    public String getRemark() {
        return Remark;
    }

    public void setRemark(String remark) {
        Remark = remark;
    }
}
