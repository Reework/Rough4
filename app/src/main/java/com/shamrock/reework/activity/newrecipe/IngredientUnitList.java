package com.shamrock.reework.activity.newrecipe;

public class IngredientUnitList {
    private String Measurement;

    private String ValueInGms;

    private String Id;

    public String getMeasurement() {
        return Measurement;
    }

    public void setMeasurement(String measurement) {
        Measurement = measurement;
    }

    public String getValueInGms() {
        return ValueInGms;
    }

    public void setValueInGms(String valueInGms) {
        ValueInGms = valueInGms;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }
}
